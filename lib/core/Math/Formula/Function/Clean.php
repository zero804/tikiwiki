class Math_Formula_Function_Clean extends Math_Formula_Function
{
	function evaluate($element)
	{
		$out = "";

        // Derived from https://stackoverflow.com/questions/14114411/remove-all-special-characters-from-a-string?utm_medium=organic&utm_source=google_rich_qa&utm_campaign=google_rich_qa
        $utf8 = array(
            '/[áàâãªä]/u'   =>   'a',
            '/[ÁÀÂÃÄ]/u'    =>   'A',
            '/[ÍÌÎÏ]/u'     =>   'I',
            '/[íìîï]/u'     =>   'i',
            '/[éèêë]/u'     =>   'e',
            '/[ÉÈÊË]/u'     =>   'E',
            '/[óòôõºö]/u'   =>   'o',
            '/[ÓÒÔÕÖ]/u'    =>   'O',
            '/[úùûü]/u'     =>   'u',
            '/[ÚÙÛÜ]/u'     =>   'U',
            '/ç/'           =>   'c',
            '/Ç/'           =>   'C',
            '/ñ/'           =>   'n',
            '/Ñ/'           =>   'N',
            '/–/'           =>   '-', // UTF-8 hyphen to "normal" hyphen
            '/[’‘‹›‚]/u'    =>   ' ', // Literally a single quote
            '/[“”«»„]/u'    =>   ' ', // Double quote
            '/ /'           =>   ' ', // nonbreaking space (equiv. to 0x160)
        );

        foreach ($element as $child) {
            $out .= preg_replace(array_keys($utf8), array_values($utf8), $this->evaluateChild($child));
		}

        return $out;
	}

}
